#!/usr/bin/env python3
"""generate_inventory.py - regenerate the README inventory block.

Scans the marketplace and computes:
- plugin count + names
- skill / agent / command / hook counts (per plugin and total)
- script counts (root scripts + plugin-local scripts)
- workflow + dependabot config counts
- Serena memory count

Writes the result between two HTML markers in README.md:
    <!-- inventory:begin -->
    ...generated content...
    <!-- inventory:end -->

Modes:
- default: rewrite README.md in place
- `--check`: exit 1 if README.md would change (CI gate to catch drift)
- `--print`: print the block to stdout without touching files

Exit codes: 0 success / no drift, 1 on drift (in --check mode) or write error.
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

BEGIN = "<!-- inventory:begin -->"
END = "<!-- inventory:end -->"


def count_glob(root: Path, pattern: str) -> int:
    return sum(1 for _ in root.glob(pattern))


def list_plugins(root: Path) -> list[dict[str, object]]:
    manifest = root / ".claude-plugin" / "marketplace.json"
    data = json.loads(manifest.read_text(encoding="utf-8"))
    plugins: list[dict[str, object]] = []
    for entry in data.get("plugins", []):
        name = entry["name"]
        plugin_root = root / "plugins" / name
        plugins.append({
            "name": name,
            "version": entry.get("version"),
            "skills": count_glob(plugin_root, "skills/*/SKILL.md"),
            "commands": count_glob(plugin_root, "commands/*.md"),
            "agents": count_glob(plugin_root, "agents/*.md"),
            "hooks_scripts": count_glob(plugin_root, "hooks/*.sh"),
            "hooks_manifest": count_glob(plugin_root, "hooks/hooks.json"),
            "scripts": count_glob(plugin_root, "scripts/*.sh") + count_glob(plugin_root, "scripts/*.py"),
            "references": count_glob(plugin_root, "references/*.md"),
            "mcp_json": count_glob(plugin_root, ".mcp.json"),
            "lsp_json": count_glob(plugin_root, ".lsp.json"),
        })
    return plugins


def render_block(root: Path) -> str:
    plugins = list_plugins(root)
    skill_total = sum(p["skills"] for p in plugins)  # type: ignore[misc]
    command_total = sum(p["commands"] for p in plugins)  # type: ignore[misc]
    agent_total = sum(p["agents"] for p in plugins)  # type: ignore[misc]
    hook_script_total = sum(p["hooks_scripts"] for p in plugins)  # type: ignore[misc]
    hook_manifest_total = sum(p["hooks_manifest"] for p in plugins)  # type: ignore[misc]
    plugin_script_total = sum(p["scripts"] for p in plugins)  # type: ignore[misc]
    reference_total = sum(p["references"] for p in plugins)  # type: ignore[misc]

    root_scripts = count_glob(root, "scripts/*.sh") + count_glob(root, "scripts/*.py")
    workflows = count_glob(root, ".github/workflows/*.yml")
    dependabot = count_glob(root, ".github/dependabot.yml")
    memories = count_glob(root, ".serena/memories/*.md")

    lines = [
        BEGIN,
        "<!-- Generated by scripts/generate_inventory.py - run `python3 scripts/generate_inventory.py` to refresh. Do not edit by hand. -->",
        "",
        f"**{len(plugins)} first-party plugins** ({skill_total} skills, {command_total} slash commands, "
        f"{agent_total} subagents, {hook_script_total} hook scripts in {hook_manifest_total} manifests, "
        f"{plugin_script_total + root_scripts} scripts total, {reference_total} references). "
        f"CI: {workflows} workflows + {dependabot} dependabot config. "
        f"Serena memories: {memories}.",
        "",
        "| Plugin | Version | Skills | Commands | Agents | Hooks | Scripts | References | MCP | LSP |",
        "|---|---|---|---|---|---|---|---|---|---|",
    ]
    for p in plugins:
        lines.append(
            f"| `{p['name']}` | `{p['version']}` | {p['skills']} | {p['commands']} | "
            f"{p['agents']} | {p['hooks_scripts']} | {p['scripts']} | {p['references']} | "
            f"{p['mcp_json']} | {p['lsp_json']} |"
        )
    lines.extend([
        "",
        END,
    ])
    return "\n".join(lines) + "\n"


def update_readme(root: Path, block: str, check_only: bool) -> int:
    readme = root / "README.md"
    if not readme.is_file():
        print("FAIL README.md not found", file=sys.stderr)
        return 1
    text = readme.read_text(encoding="utf-8")
    if BEGIN not in text or END not in text:
        if check_only:
            # SKIP gracefully so the harness does not block until G15 adds
            # markers. After markers exist, --check enforces drift.
            print(
                f"SKIP README.md does not contain {BEGIN}/{END} markers yet; "
                f"add them once and `--check` becomes enforcement."
            )
            return 0
        print(
            f"WARN README.md does not contain {BEGIN}/{END} markers. Add them manually "
            f"where you want the inventory block to appear; this script will not insert markers.",
            file=sys.stderr,
        )
        return 1
    pre, _, rest = text.partition(BEGIN)
    _, _, post = rest.partition(END)
    # post starts immediately after END; strip the leading newline so we keep one.
    if post.startswith("\n"):
        post = post[1:]
    new_text = pre + block + post
    if new_text == text:
        print("OK README.md inventory already current")
        return 0
    if check_only:
        print("FAIL README.md inventory is stale; run `python3 scripts/generate_inventory.py`", file=sys.stderr)
        return 1
    readme.write_text(new_text, encoding="utf-8")
    print("OK README.md inventory updated")
    return 0


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--check", action="store_true", help="exit 1 on drift, do not write")
    parser.add_argument("--print", action="store_true", help="print block to stdout only")
    args = parser.parse_args()

    root = Path(__file__).resolve().parent.parent
    block = render_block(root)
    if args.print:
        sys.stdout.write(block)
        return 0
    return update_readme(root, block, check_only=args.check)


if __name__ == "__main__":
    raise SystemExit(main())
