#!/usr/bin/env python3
"""Generate docs/contract-matrix.md from config/rldyour-contract.json."""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path
from typing import Any

CONTRACT_PATH = Path("config/rldyour-contract.json")
OUTPUT_PATH = Path("docs/contract-matrix.md")


def _load_json(path: Path) -> dict[str, Any]:
    payload = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(payload, dict):
        raise ValueError(f"{path} must contain a JSON object")
    return payload


def _cell(adapter: dict[str, Any] | None) -> str:
    if not adapter:
        return "missing"
    for key in ("invocation", "name", "plugin", "status"):
        value = adapter.get(key)
        if isinstance(value, str) and value:
            return value
    path = adapter.get("path")
    if isinstance(path, str) and path:
        return f"`{path}`"
    workflow = adapter.get("workflow")
    if isinstance(workflow, str) and workflow:
        return f"`{workflow}`"
    return "mapped"


def _escape(value: str) -> str:
    return value.replace("|", "\\|")


def _table(headers: list[str], rows: list[list[str]]) -> list[str]:
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(_escape(cell) for cell in row) + " |")
    return lines


def _adapter_rows(entries: list[Any], *, include_claude_path: bool = False) -> list[list[str]]:
    rows: list[list[str]] = []
    for entry in entries:
        if not isinstance(entry, dict):
            continue
        claude_raw = entry.get("claude")
        claude: dict[str, Any] = claude_raw if isinstance(claude_raw, dict) else {}
        row = [
            str(entry.get("id", "")),
            str(entry.get("description", "")),
            _cell(claude),
            _cell(entry.get("codex") if isinstance(entry.get("codex"), dict) else None),
            _cell(entry.get("opencode") if isinstance(entry.get("opencode"), dict) else None),
            _cell(entry.get("gemini") if isinstance(entry.get("gemini"), dict) else None),
            _cell(entry.get("mimocode") if isinstance(entry.get("mimocode"), dict) else None),
        ]
        if include_claude_path:
            path = claude.get("path") or claude.get("script") or claude.get("workflow") or ""
            row.append(f"`{path}`" if path else "")
        rows.append(row)
    return rows


def render(contract: dict[str, Any]) -> str:
    lines: list[str] = [
        "# rldyour Cross-Tool Contract Matrix",
        "",
        "<!-- Generated by scripts/generate_contract_matrix.py from config/rldyour-contract.json. Do not edit by hand. -->",
        "",
        "This matrix is the human-readable projection of the machine-readable rldyour contract. It documents how the same business mechanism maps to Claude Code, Codex, OpenCode, Gemini CLI, and MiMoCode. The local validator proves the Claude Code adapter against files in this repository and requires non-local adapters to be explicitly mapped or marked adapter-specific.",
        "",
        f"- Schema version: `{contract.get('schema_version')}`",
        f"- Verified on: `{contract.get('verified_on')}`",
        f"- Owner repository: `{contract.get('owner_repo')}`",
        "",
        "## Domains",
        "",
    ]

    domains = contract.get("domains", [])
    lines.extend(
        _table(
            ["ID", "Description", "Claude", "Codex", "OpenCode", "Gemini", "MiMoCode"],
            _adapter_rows(domains if isinstance(domains, list) else []),
        )
    )

    sections = [
        ("Public Flows", "public_flows", True),
        ("Skills", "skills", True),
        ("Agent Roles", "agent_roles", True),
        ("Hook Lifecycle", "hook_lifecycle", True),
        ("CI Baseline", "ci_baseline", False),
    ]
    for title, key, include_path in sections:
        lines.extend(["", f"## {title}", ""])
        entries = contract.get(key, [])
        headers = ["ID", "Description", "Claude", "Codex", "OpenCode", "Gemini", "MiMoCode"]
        if include_path:
            headers.append("Claude Path")
        lines.extend(_table(headers, _adapter_rows(entries if isinstance(entries, list) else [], include_claude_path=include_path)))

    lines.append("")
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--check", action="store_true", help="fail if docs/contract-matrix.md is stale")
    args = parser.parse_args()

    root = Path(__file__).resolve().parent.parent
    contract = _load_json(root / CONTRACT_PATH)
    rendered = render(contract)
    output_path = root / OUTPUT_PATH

    if args.check:
        if not output_path.is_file():
            print(f"FAIL {OUTPUT_PATH.as_posix()} is missing", file=sys.stderr)
            return 1
        current = output_path.read_text(encoding="utf-8")
        if current != rendered:
            print(
                f"FAIL {OUTPUT_PATH.as_posix()} is stale; run `python3 scripts/generate_contract_matrix.py`",
                file=sys.stderr,
            )
            return 1
        print("OK contract matrix is current")
        return 0

    output_path.write_text(rendered, encoding="utf-8")
    print(f"wrote {OUTPUT_PATH.as_posix()}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
